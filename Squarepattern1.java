class Squarepattern1 {
    public static void main(String[] args) {
        int n = 6, j, i;
        for (i = 1; i <= n; i++) {
            System.out.print(i);
            for (j = 1; j <= 6; j++) {
                System.out.print(j);
            }
            System.out.println();

        }
    }
}
