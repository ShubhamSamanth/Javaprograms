class Squarepattern {
    public static void main(String[] args) {
        int n = 6, j, i;
        for (i = 0; i <= 6; i++) {
            System.out.print("*");
            for (j = 0; j < n; j++) {
                System.out.print(" *");
            }
            System.out.println();

        }
    }
}
