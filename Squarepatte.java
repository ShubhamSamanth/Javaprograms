class Squarepatte {
    public static void main(String[] args) {
        int n = 6, j, i;
        for (i = 0; i <= n - 1; i++) {
            System.out.print("*");
            for (j = 0; j <= n; j++) {
                System.out.print("*");
            }

            System.out.println();
        }
    }
}